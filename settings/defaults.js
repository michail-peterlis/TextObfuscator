async function storeDefaults(e) {
    let l = [];
    defaultOptions().forEach((e) => l.push(e.name));
    let constructJson = function (jsonKey, jsonValue) {
        var jsonObj = {};
        jsonObj[jsonKey] = jsonValue;
        return jsonObj;
    }
    
    let promises = [];
    promises.push(browser.storage.sync.clear()
    .then(() => { return browser.storage.sync.set({ LevelList: l });})
    .then(() => { defaultOptions().forEach((e) => promises.push(browser.storage.sync.set(constructJson(e.name, e.table))));}));
    
    await Promise.all(promises);
    
    if(e != undefined)
        e.preventDefault();
}


